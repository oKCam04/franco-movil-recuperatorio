void main() {
  int numero1 = 10;
  int numero2 = 5;

  // Comparaciones
  bool sonIguales = numero1 == numero2;
  bool esMayor = numero1 > numero2;
  bool sonDiferentes = numero1 != numero2;

  // Imprimir resultados
  print('¿$numero1 es igual a $numero2? $sonIguales');
  print('¿$numero1 es mayor que $numero2? $esMayor');
  print('¿$numero1 es diferente de $numero2? $sonDiferentes');
}
