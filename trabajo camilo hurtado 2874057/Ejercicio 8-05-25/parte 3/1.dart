void main() {
  // Declarar dos números
  int a = 10;
  int b = 5;

  // Operaciones aritméticas
  int suma = a + b;
  int resta = a - b;
  int multiplicacion = a * b;
  double division = a / b;

  // Imprimir resultados
  print('Suma: $suma');
  print('Resta: $resta');
  print('Multiplicación: $multiplicacion');
  print('División: $division');
}
